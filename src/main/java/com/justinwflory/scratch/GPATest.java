public class GPA{

     public static void main(int g){
        if (g >= 90) {
            gpa = 4;
        }
        else if (g >= 80) {
            gpa = 3;
        }
        else if (g >= 70) {
            gpa = 2;
        }
        else if (g >= 60) {
            gpa = 1;
        }
        else {
            gpa = 0;
        }
        return g;
     }
}
